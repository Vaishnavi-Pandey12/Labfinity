export function getExperimentsBySubjectName(subjectName: string) {
  return experiments.filter((experiment) => experiment.subjectName === subjectName);
}

export function findExperimentBySubjectAndTitle(subjectName: string, title: string) {
  return experiments.find(
    (experiment) => experiment.subjectName === subjectName && experiment.title === title,
  );
}
